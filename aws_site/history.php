					<h1>History</h1>
					<p>REDI was born out of six years of extensive research on how to innovatively solve poverty in rural areas through improving small businesses owned by poor entrepreneurs. From 2007 to 2013 the current President of REDI, Levy Odera, was performing research for his PhD on problems and solutions for poor small business owners in Kenya, as well as conducting research for another project assessing the business environment in Kenya and other African countries. During this time he also worked with a women’s organization there to help them establish a business. In January 2013, Levy put together the vast knowledge and experiences he had acquired to design an innovative model for effectively solving poverty in rural areas. He named the model “Solving Poverty In Real Time (SPIReT).” The next stage was to implement it in the rural community he had been working in. </p>
					<br/>
					<p>The women’s organization decided to start a poultry farming business to alleviate their poverty. In June 2013 Levy took a team of 15 passionate volunteers to Kenya to initiate the project under the SPIReT model. The volunteer team worked with the women to address several socioeconomic and political needs that Levy had identified in the community. As the team worked together, it was deliberating on ideas for improving the SPIReT model and ways to help the project achieve long-term successful and help other rural entrepreneurs in developing countries. To fulfil this mission Levy established REDI in June 2013, which would provide a platform for the volunteer team to continue working together to improve living standards in rural areas through the model. Since June, REDI has been intensively working with the women’s organization to develop the business they created.</p>
